// Switch Statements 
// for example You have to write the days according to the Week Switch statements can help you with that

let day=0;

switch(day){
    case 0:
    console.log("Sunday");
    break;
    case 1:
    console.log("monday");
    break;
    case 2:
    console.log("Tuesday");
    break;
    case 3:
    console.log("Wednesday");
    break;
    case 4:
    console.log("Thursday");
    break;
    case 5:
    console.log("Friday");
    break;
  default:
    console.log("invalid day");
}
